CREATE TABLE Company(
	CompanyId INT IDENTITY(1,1) PRIMARY KEY,
	CompanyName VARCHAR(200) NoT NULL,
	Tagline VARCHAR(500) NULL,
	Phone BIGINT NOT NULL,
	AlternatePhone BIGINT NULL,
	Email VARCHAR(100) NULL,
	Website VARCHAR(200) NULL,
	Facebook VARCHAR(200) NULL,
	Instagram VARCHAR(200) NULL,
	Youtube VARCHAR(200) NULL,
	Whatsapp VARCHAR(200) NULL,
	GstNumber VARCHAR(20) NULL,
	PanNumber VARCHAR(15) NULL,
	CinNumber VARCHAR(25) NULL,
	BankName VARCHAR(100) NULL,
	AccountNumber VARCHAR(30) NULL,
	AccountHolderName VARCHAR(200) NULL,
	IfscCode VARCHAR(15) NULL,
	BranchName VARCHAR(100) NULL,
	UpiId VARCHAR(100) NULL,
	InvoicePrefix VARCHAR(10) NULL DEFAULT 'INV',
	InvoiceStartNumber INT NULL DEFAULT 1001,
	TermsConditions VARCHAR(MAX) NULL,
	FooterNote VARCHAR(500) NULL,
	LogonName VARCHAR(100) NULL,
	LogoPath VARCHAR(MAX) NULL,
	SignaturenName VARCHAR(100) NULL,
	SignaturePath VARCHAR(MAX) NULL,
	Status INT NOT NULL,
	CreatedBy INT Not Null,
	CreatedDate DATETIME DEFAULT GETDATE() NOT NULL,
	ModifiedBy INT null,
	ModifiedDate DATETIME NULL
);